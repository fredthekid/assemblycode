stack_Segment SEGMENT STACK 'STACK'
	DB 64 DUP(?)
stack_Segment ENDS

data_Segment SEGMENT PUBLIC 'DATA'
	CR EQU 0DH; moves cursor back to the left
	LF EQU 0AH; moves cursor down 1 line(enter)
data_Segment ENDS

code_Segment SEGMENT PUBLIC 'CODE'
MAIN PROC FAR
	ASSUME CS:code_Segment, DS:data_Segment, SS:stack_Segment
	PUSH DS
	MOV AX,0
	PUSH AX
	MOV AX,data_Segment
	MOV DS,AX
	
asknumber:
	;NEXT LINE
	MOV AH,02H
	MOV DL,0AH
	INT 21H
	
	;DISPLAY 'N'
	MOV AH,02H
	MOV DL,4EH
	INT 21H
	
	;GET USER NUMBER
	MOV AH,01H
	INT 21H

checkifvalid:
	CMP AL,31H
	JLE asknumber
	CMP AL,3AH
	JL continue
	
	SUB AL,07H
	CMP AL,40H
	JGE asknumber
	CMP AL,39H
	JLE asknumber
	
continue:
	;GET NUMBER INTO A SINGLE NUMBER
	AND AX,000FH
	MOV CX,AX
	ADD AX,200H
	MOV SI,AX
	MOV AX,00FFH
	MOV [SI],AX
	MOV SI,0200H
	PUSH CX
	MOV BL,31H

MONUMBERS:
	;NEXT LINE
	MOV AH,02H
	MOV DL,0AH
	INT 21H
	
	;DISPLAY CURRENT NUMBER COUNT
	MOV AH,02H
	MOV DL,BL
	INT 21H
	
	;ASK FOR NUMBER AND STORE
	MOV AH, 01H
	INT 21H
	
checkifvalid2:
	CMP AL,2FH
	JLE MONUMBERS
	CMP AL,3AH
	JL INCREMENT
	
	SUB AL,07H
	CMP AL,40H
	JGE MONUMBERS
	CMP AL,39H
	JLE MONUMBERS
	
INCREMENT:
	MOV [SI],AL
	CMP BL,39H
	JNE ABCDEFADJUST
	ADD BL,07H

ABCDEFADJUST:
	INC BL
	INC SI
	DEC CX
	JNZ MONUMBERS
	
	POP CX
	MOV SI,0200H
	MOV BL,30H
	MOV DI,00FFH
	
	;NEW LINE
	MOV AH,02H
	MOV DL,0AH
	INT 21H
	
STARTDISPLAY:
	CMP [SI],BL
	JNE NOTEQUAL
	MOV AH,02H
	MOV DL,BL
	INT 21H
	
	;NEW LINE
	MOV AH,02H
	MOV DL,0AH
	INT 21H

NOTEQUAL:
	INC SI
	CMP [SI],DI
	JNE STARTDISPLAY
	CMP BL,39H
	JNE KEEPGOINGDOE
	ADD BL,07H

KEEPGOINGDOE:
	MOV SI,0200H
	INC BL
	CMP BL,46H
	JNE STARTDISPLAY
	RET
	
MAIN ENDP

code_Segment ENDS
	END MAIN
